package week1.day2;

public class JavaChallenge1 {

	public static void main(String[] args) {
		double x=8;
		System.out.println("The square root of the given number is :"+Math.sqrt(x));
		double y=Math.sqrt(x);
		System.out.println("The round off number is :"+Math.round(y));
		int z=(int)y;//type casting-converting one data type to another data type
		System.out.println("The truncated number is :"+z);
		
	}

}

